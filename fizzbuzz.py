def fizzbuzz(n):
    for i in range(1, n + 1):
        if i % 3 == 0 and i % 5 == 0:
            print("FizzBuzz")
        elif i % 3 == 0:
            print("Fizz")
        elif i % 5 == 0:
            print("Buzz")
        else:
            print(i)

if __name__ == "__main__":
    try:
        limit = int(input("Enter the upper limit for FizzBuzz: "))
        fizzbuzz(limit)
    except ValueError:
        print("Please enter a valid integer.")
